import java.util.*;

public class Solution {

    public static void main(String args[]){
        Scanner s = new Scanner(System.in);
        int a = s.nextInt();
        int b= s.nextInt();
        int c = s.nextInt();
        System.out.println(a + "\n" + b + "\n" + c);
    }
}